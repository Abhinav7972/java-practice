public class ifelse 
{
    public static void main(String[] args)
     {
        int k =10,j=5;
        System.out.println("first value is :" + k);
        System.out.println("second value is :" +j);
        if(k>j)
        {
            System.out.println(k + "    is greater");
        }
        else
        {
            System.out.println(j + "   is greater");
        }
    }
}
